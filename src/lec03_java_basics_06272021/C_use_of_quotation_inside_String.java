package lec03_java_basics_06272021;

public class C_use_of_quotation_inside_String {

	public static void main(String[] args) {
		System.out.println("My Name is Tofael. I am your instructor.");
		System.out.println("My Name is \'Tofael\'. I am your instructor.");
		System.out.println("My Name is \"Tofael\". I am your instructor.");
		System.out.println("My Name is \\Tofael\\. I am your instructor."); // common mistake -- missing of ;
		System.out.println("Tofael"); // common mistake -- missing of " "

	} // common mistake -- missing of }

}
